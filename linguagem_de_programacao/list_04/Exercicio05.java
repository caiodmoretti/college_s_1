/* Exercício 05
Escreva um programa que imprima todos os números inteiros de 200 a 100.
Após a leitura, os números deverão apresentada na tela.
*/



public class Exercicio05 {
	public static void main(String[] args) {
		int  contador;
		System.out.println("Confira a os números inteiros de 200 a 100!");
    	
    	
	    for (contador = 200; contador >= 100; contador--) {

            System.out.println(contador);
	    }
	    
	}
}


